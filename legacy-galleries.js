function createLegacyGalleryItem(group, item, groupIndex, itemIndex) {
  const button = document.createElement("button");
  button.type = "button";
  button.className = "legacy-gallery-item";
  button.dataset.groupIndex = String(groupIndex);
  button.dataset.itemIndex = String(itemIndex);
  button.setAttribute("aria-label", `${group.title}: ${item.title}`);

  const frame = document.createElement("div");
  frame.className = "legacy-gallery-frame";

  const image = document.createElement("img");
  image.src = item.src;
  image.alt = item.caption || item.title;
  image.loading = "lazy";
  frame.appendChild(image);

  const meta = document.createElement("div");
  meta.className = "legacy-gallery-meta";

  const type = document.createElement("div");
  type.className = "legacy-gallery-type";
  type.textContent = item.type;

  const title = document.createElement("div");
  title.className = "legacy-gallery-title";
  title.textContent = item.title;

  meta.append(type, title);
  button.append(frame, meta);

  return button;
}

function createLegacyGalleryGroup(group, groupIndex) {
  const section = document.createElement("section");
  section.className = "legacy-gallery-group";
  section.id = group.slug;

  const head = document.createElement("div");
  head.className = "legacy-gallery-head";

  const eyebrow = document.createElement("div");
  eyebrow.className = "micro-label";
  eyebrow.textContent = group.eyebrow;

  const title = document.createElement("h3");
  title.textContent = group.title;

  const description = document.createElement("p");
  description.className = "body";
  description.textContent = group.description;

  head.append(eyebrow, title, description);

  const grid = document.createElement("div");
  grid.className = "legacy-gallery-grid";

  group.items.forEach((item, itemIndex) => {
    grid.appendChild(createLegacyGalleryItem(group, item, groupIndex, itemIndex));
  });

  section.append(head, grid);
  return section;
}

function createLegacyGalleryFlatGrid(group) {
  const grid = document.createElement("div");
  grid.className = "legacy-gallery-grid";

  group.items.forEach((item, itemIndex) => {
    grid.appendChild(createLegacyGalleryItem(group, item, 0, itemIndex));
  });

  return grid;
}

function buildAssetLightbox() {
  const lightbox = document.createElement("div");
  lightbox.className = "asset-lightbox";
  lightbox.hidden = true;
  lightbox.innerHTML = `
    <button class="asset-lightbox-backdrop" type="button" aria-label="Close image preview"></button>
    <div class="asset-lightbox-dialog" role="dialog" aria-modal="true" aria-label="Asset preview">
      <div class="asset-lightbox-stage">
        <figure class="asset-lightbox-figure">
          <div class="report-page-frame">
            <img class="asset-lightbox-image" alt="" />
          </div>
        </figure>
      </div>
      <div class="asset-lightbox-meta">
        <div class="asset-lightbox-head">
          <div>
            <div class="asset-lightbox-kicker"></div>
            <div class="asset-lightbox-title"></div>
          </div>
          <button class="asset-lightbox-close" type="button" aria-label="Close image preview">Close</button>
        </div>
        <div class="asset-lightbox-copy">
          <p class="body asset-lightbox-caption"></p>
        </div>
        <div class="asset-lightbox-nav">
          <button class="asset-lightbox-prev" type="button">Previous</button>
          <button class="asset-lightbox-next" type="button">Next</button>
        </div>
      </div>
    </div>
  `;

  document.body.appendChild(lightbox);
  return lightbox;
}

function initLegacyGalleries() {
  const hosts = Array.from(document.querySelectorAll("[data-legacy-gallery-area]"));
  const zoomTriggers = Array.from(document.querySelectorAll("[data-image-zoom]"));
  if (!hosts.length && !zoomTriggers.length) return;

  const lightbox = buildAssetLightbox();
  const backdrop = lightbox.querySelector(".asset-lightbox-backdrop");
  const closeButton = lightbox.querySelector(".asset-lightbox-close");
  const prevButton = lightbox.querySelector(".asset-lightbox-prev");
  const nextButton = lightbox.querySelector(".asset-lightbox-next");
  const image = lightbox.querySelector(".asset-lightbox-image");
  const kicker = lightbox.querySelector(".asset-lightbox-kicker");
  const title = lightbox.querySelector(".asset-lightbox-title");
  const caption = lightbox.querySelector(".asset-lightbox-caption");
  const nav = lightbox.querySelector(".asset-lightbox-nav");

  const galleryState = {
    groups: [],
    currentGroupIndex: 0,
    currentItemIndex: 0,
    singleMode: false,
    lastTrigger: null,
  };

  function getFocusableInDialog() {
    const focusable = lightbox.querySelectorAll(
      'a[href], button:not([disabled]), [tabindex]:not([tabindex="-1"])'
    );
    return Array.from(focusable).filter((el) => !el.hidden && el.offsetParent !== null);
  }

  function renderLightboxItem() {
    const group = galleryState.groups[galleryState.currentGroupIndex];
    if (!group) return;
    const item = group.items[galleryState.currentItemIndex];
    if (!item) return;

    image.src = item.src;
    image.alt = item.caption || item.title;
    title.textContent = item.title;
    kicker.textContent = `${group.title} · ${item.type}`;
    caption.textContent = item.caption || "";
    prevButton.disabled = galleryState.currentItemIndex === 0;
    nextButton.disabled = galleryState.currentItemIndex === group.items.length - 1;
  }

  function openLightbox(groupIndex, itemIndex, trigger) {
    galleryState.lastTrigger = trigger || document.activeElement;
    galleryState.singleMode = false;
    if (nav) nav.hidden = false;
    galleryState.currentGroupIndex = groupIndex;
    galleryState.currentItemIndex = itemIndex;
    renderLightboxItem();
    lightbox.hidden = false;
    document.body.classList.add("asset-lightbox-open");
    closeButton.focus();
  }

  function openSingleImage({ src, title: itemTitle, kicker: itemKicker, caption: itemCaption, alt, trigger }) {
    galleryState.lastTrigger = trigger || document.activeElement;
    galleryState.singleMode = true;
    if (nav) nav.hidden = true;
    image.src = src;
    image.alt = alt || itemCaption || itemTitle || "";
    title.textContent = itemTitle || "";
    kicker.textContent = itemKicker || "";
    caption.textContent = itemCaption || "";
    lightbox.hidden = false;
    document.body.classList.add("asset-lightbox-open");
    closeButton.focus();
  }

  function closeLightbox() {
    lightbox.hidden = true;
    document.body.classList.remove("asset-lightbox-open");
    const restore = galleryState.lastTrigger;
    galleryState.lastTrigger = null;
    if (restore && typeof restore.focus === "function") {
      restore.focus();
    }
  }

  function stepLightbox(direction) {
    if (galleryState.singleMode) return;
    const group = galleryState.groups[galleryState.currentGroupIndex];
    if (!group) return;
    const nextIndex = galleryState.currentItemIndex + direction;
    if (nextIndex < 0 || nextIndex >= group.items.length) return;
    galleryState.currentItemIndex = nextIndex;
    renderLightboxItem();
  }

  hosts.forEach((host) => {
    const areaKey = host.dataset.legacyGalleryArea;
    const area = legacyGalleryAreas[areaKey];
    if (!area) return;
    host.classList.add("legacy-gallery-stack");

    let groupsForLightbox = area.groups;

    if (Array.isArray(area.items)) {
      const syntheticGroup = {
        title: area.label,
        items: area.items,
      };

      groupsForLightbox = [syntheticGroup];
      host.appendChild(createLegacyGalleryFlatGrid(syntheticGroup));
    } else {
      area.groups.forEach((group, groupIndex) => {
        host.appendChild(createLegacyGalleryGroup(group, groupIndex));
      });
    }

    host.addEventListener("click", (event) => {
      const button = event.target.closest(".legacy-gallery-item");
      if (!button || !host.contains(button)) return;
      galleryState.groups = groupsForLightbox;
      openLightbox(Number(button.dataset.groupIndex), Number(button.dataset.itemIndex), button);
    });
  });

  zoomTriggers.forEach((trigger) => {
    if (!trigger.hasAttribute("tabindex")) trigger.tabIndex = 0;
    if (!trigger.hasAttribute("role")) trigger.setAttribute("role", "button");

    const open = () => {
      const img = trigger.querySelector("img");
      const captionEl = trigger.querySelector("figcaption");
      openSingleImage({
        src: trigger.dataset.zoomSrc || (img && img.src) || "",
        title: trigger.dataset.zoomTitle || "",
        kicker: trigger.dataset.zoomKicker || "",
        caption: trigger.dataset.zoomCaption || (captionEl && captionEl.textContent.trim()) || "",
        alt: img && img.alt,
        trigger,
      });
    };

    trigger.addEventListener("click", (event) => {
      event.preventDefault();
      open();
    });

    trigger.addEventListener("keydown", (event) => {
      if (event.key === "Enter" || event.key === " ") {
        event.preventDefault();
        open();
      }
    });
  });

  backdrop.addEventListener("click", closeLightbox);
  closeButton.addEventListener("click", closeLightbox);
  prevButton.addEventListener("click", () => stepLightbox(-1));
  nextButton.addEventListener("click", () => stepLightbox(1));

  document.addEventListener("keydown", (event) => {
    if (lightbox.hidden) return;
    if (event.key === "Escape") {
      closeLightbox();
    } else if (event.key === "ArrowLeft") {
      stepLightbox(-1);
    } else if (event.key === "ArrowRight") {
      stepLightbox(1);
    } else if (event.key === "Tab") {
      const focusable = getFocusableInDialog();
      if (focusable.length === 0) return;
      const first = focusable[0];
      const last = focusable[focusable.length - 1];
      if (event.shiftKey && document.activeElement === first) {
        event.preventDefault();
        last.focus();
      } else if (!event.shiftKey && document.activeElement === last) {
        event.preventDefault();
        first.focus();
      }
    }
  });
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initLegacyGalleries);
} else {
  initLegacyGalleries();
}
